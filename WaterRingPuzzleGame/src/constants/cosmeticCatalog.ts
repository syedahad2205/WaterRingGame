// ── Lookup map and helpers ────────────────────────────────────

export const COSMETIC_MAP: Record<string, CosmeticDefinition> = {};
for (const item of COSMETIC_CATALOG) {
  COSMETIC_MAP[item.id] = item;
}

export function getCosmeticById(id: string): CosmeticDefinition | undefined {
  return COSMETIC_MAP[id];
}

export function getCosmeticsByCategory(category: CosmeticCategory): CosmeticDefinition[] {
  return COSMETIC_CATALOG.filter(c => c.category === category);
}

export function getCosmeticsByRarity(rarity: CosmeticRarity): CosmeticDefinition[] {
  return COSMETIC_CATALOG.filter(c => c.rarity === rarity);
}

export function getStoreItems(): CosmeticDefinition[] {
  return COSMETIC_CATALOG.filter(c => c.source === 'store');
}
