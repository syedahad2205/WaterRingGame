// ── Type definitions ──────────────────────────────────────────

export type CosmeticRarity = 'common' | 'rare' | 'epic' | 'legendary' | 'mythic';

export type CosmeticCategory =
  | 'ring_skin'
  | 'particle_trail'
  | 'water_style'
  | 'peg_skin'
  | 'victory_animation'
  | 'button_skin';

export type CosmeticSource =
  | 'store'
  | 'achievement'
  | 'daily_reward'
  | 'collection_bonus'
  | 'event'
  | 'prestige';

export interface RingSkinConfig {
  colors: Record<string, { fill: string; stroke: string; glow: string }>;
  shimmer?: boolean;
  texture?: 'none' | 'metallic' | 'matte' | 'glossy' | 'holographic' | 'crystalline' | 'brushed';
}

export interface PegSkinConfig {
  base: string;
  tip: string;
  glow: string;
  occupied: string;
}

export interface WaterStyleConfig {
  deep: string;
  mid: string;
  surface: string;
  foam: string;
  tint: [number, number, number];
  opacity: number;
  waveAmplitude: number;
  waveSpeed: number;
}

export interface ParticleTrailConfig {
  colors: string[];
  speed: number;
  size: number;
  count: number;
  shape: 'circle' | 'star' | 'square' | 'heart' | 'diamond' | 'snowflake' | 'note' | 'leaf' | 'spark';
}

export interface VictoryAnimConfig {
  particleType: 'burst' | 'rain' | 'spiral' | 'fountain' | 'cascade';
  colors: string[];
  count: number;
  duration: number;
}

export interface ButtonSkinConfig {
  gradient: [string, string];
  glowColor: string;
}

export type CosmeticConfig =
  | RingSkinConfig
  | PegSkinConfig
  | WaterStyleConfig
  | ParticleTrailConfig
  | VictoryAnimConfig
  | ButtonSkinConfig;

export interface CosmeticDefinition {
  id: string;
  name: string;
  description: string;
  category: CosmeticCategory;
  rarity: CosmeticRarity;
  coinCost: number;
  source: CosmeticSource;
  previewColors: {
    primary: string;
    secondary: string;
  };
  config: CosmeticConfig;
}

